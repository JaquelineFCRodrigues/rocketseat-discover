// Operadores lógicos (logical operators)

// 2 valores booleanos, quando verificados, resultará em verdadeiro ou falso.

let pao = true;
let queijo = true;

// AND && (precisa dos 2)

console.log(pao && queijo);

// OR || ( precisa de pelo menos 1)

console.log(pao || queijo);

// NOT ! (nega a condição)

console.log(!pao);