// Operadores de comparação estritamente igual e estritamente diferente

// Irá comparar valores e tipos e retorna um Boolean como resposta à comparação

let one = 1
let two = 2

// === estritamente igual a
console.log(one === "1");
console.log(one === 1);

// !== estritamente diferente de
console.log(two !== "2");
console.log(two !== 2);